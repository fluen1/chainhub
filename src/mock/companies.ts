export function getCompanies(scenario: DataScenario = 'normal', count?: number): MockCompany[] {
  if (scenario === 'empty') return []

  let companies = [...mockCompanies]

  if (scenario === 'many_warnings') {
    // Eskalér alle healthy til warning for demo-scenariet
    companies = companies.map((c) =>
      c.healthStatus === 'healthy'
        ? { ...c, healthStatus: 'warning' as const, healthReasons: ['Afventer kvartalsopdatering'] }
        : c
    )
  }

  if (count !== undefined) {
    return companies.slice(0, count)
  }

  return companies
}

export function getCompanyById(id: string): MockCompany | undefined {
  return mockCompanies.find((c) => c.id === id)
}
